#Definición de función main
def main():
    print("Ejercicio 1 - Listas Rangos Ciclos")
    print()

    # Range crea un rango, range(inicio, fin, paso)
    rango=range(100,103)

    # Con list(rango) lo pasamos a formato lista
    print(list(rango))

    print()
    print("Fin de programa")
    

if __name__ == "__main__":
    main()
